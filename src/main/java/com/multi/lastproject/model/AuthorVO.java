package com.multi.lastproject.model;

public class AuthorVO {
	private String authorId;
	private String authorName;
	private String authorNick;
	private String authorHp;
	private String authorClass;
	private String authorDescript;
	private String authorEmail;
	private String authorAddress;
	private String authorImage;

	public String getAuthorId() {
		return authorId;
	}

	public void setAuthorId(String authorId) {
		this.authorId = authorId;
	}

	public String getAuthorName() {
		return authorName;
	}

	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}

	public String getAuthorNick() {
		return authorNick;
	}

	public void setAuthorNick(String authorNick) {
		this.authorNick = authorNick;
	}

	public String getAuthorHp() {
		return authorHp;
	}

	public void setAuthorHp(String authorHp) {
		this.authorHp = authorHp;
	}

	public String getAuthorClass() {
		return authorClass;
	}

	public void setAuthorClass(String authorClass) {
		this.authorClass = authorClass;
	}

	public String getAuthorDescript() {
		return authorDescript;
	}

	public void setAuthorDescript(String authorDescript) {
		this.authorDescript = authorDescript;
	}

	public String getAuthorEmail() {
		return authorEmail;
	}

	public void setAuthorEmail(String authorEmail) {
		this.authorEmail = authorEmail;
	}

	public String getAuthorAddress() {
		return authorAddress;
	}

	public void setAuthorAddress(String authorAddress) {
		this.authorAddress = authorAddress;
	}

	public String getAuthorImage() {
		return authorImage;
	}

	public void setAuthorImage(String authorImage) {
		this.authorImage = authorImage;
	}
}
